<h1>Yachtino Wordpress Plugin für Yachtall.com/Happycharter.com</h1>
Sie können Ihre Boote, die Sie auf yachtall.com oder happycharter.com haben, auch in Ihre eigene Webseite einbinden, wenn Sie Wordpress benutzen.<br />
Damit das Wordpress Plugin mit unserer Datenbank kommuniziert, brauchen Sie Ihren Application Key für das Plugin.<br />
Kontaktieren Sie uns bitte <a href="https://www.happycharter.com/de/info/contact" target="_blank">auf Happycharter.com</a> oder
<a href="https://www.yachtall.com/de/info/contact" target="_blank">auf Yachtall.com</a> und fragen nach dem Key für Wordpress Plugin.<br /><br />

Wenn Sie den Code haben, laden Sie folgende Datei herunter:<br /><br />
<a href="https://update.yachtino.com/wordpress/shiplisting.zip">https://update.yachtino.com/wordpress/shiplisting.zip</a><br /><br />
Danach laden Sie sie bei Wordpress hoch und installieren + aktivieren Sie das Plugin.<br /><br />

Nach der Aktivierung müssen Sie Ihren Application Key eingeben, damit Sie Ihre Boote von Yachtino bekommen.<br />
Gehen Sie zu <b>Shiplisting->Shiplisting</b> und tragen Sie "<b>Application Key</b>" und "<b>Application Site&nbsp;ID</b>" ein.<br /><br />
<b>Layout Settings</b><br />
<i><b>Advanced Layout Filtering</b></i> - Wenn Sie es aktivieren, wird ein Suchformular in der Bootsliste angezeigt (z.B. Suche nach Bootslänge, Preis...).<br />
<i><b>Layout Contact Form</b></i> - Hier können Sie auch einstellen, ob bei der Bootsdetailansicht ein Kontaktformular für die Anfragen angezeigt werden soll und die Anfragen über unseren Server verschickt werden sollen.
<br /><br />

Nachdem Sie die Einstellungen gespeichert haben, gehen Sie auf Shiplisting->Generator und erstellen Sie Ihre Seiten.<br />
Es wird unterschieden zwischen Bootsliste (List View) und Bootsdetailansicht (Detail View).<br />
Es muss mindestens eine Bootsdetailansicht definiert sein.<br />

Der Rest sollte selbsterklärend sein.
